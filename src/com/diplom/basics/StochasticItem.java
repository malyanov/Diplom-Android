package com.diplom.basics;

public class StochasticItem {
	private int slow, fast;

	public StochasticItem(int slow, int fast) {
		super();
		this.slow = slow;
		this.fast = fast;
	}

	public int getSlow() {
		return slow;
	}

	public void setSlow(int slow) {
		this.slow = slow;
	}

	public int getFast() {
		return fast;
	}

	public void setFast(int fast) {
		this.fast = fast;
	}
	
}
